// фибоначи
int Fibonacci( int n)
{
    if(n == 1 || n == 2) return 1;
    else Fibonacci(n - 1) + Fibonacci(n - 2);
}
for (int i = 0; i < 10; i++)
{
    Console.WriteLine(Fibonacci(i));
    
